-- Procedimento que nos fornece uma lista com os livros de uma dada 
 -- editora, cujo código é passado como parâmetro.

(
  v_CódigoEditora IN NUMBER,
  cv_1 OUT SYS_REFCURSOR
)
RETURN NUMBER
AS
   v_temp NUMBER(1, 0) := 0;

BEGIN

   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE NOT EXISTS ( SELECT * 
                          FROM Editoras 
                            WHERE Número = v_CódigoEditora );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;
      
   IF v_temp = 1 THEN
   
   BEGIN
      DBMS_OUTPUT.PUT_LINE('Código de Editora Inválido' || UTILS.CONVERT_TO_VARCHAR2(v_CódigoEditora,5));
      DBMS_OUTPUT.PUT_LINE(' ');
      RETURN 9;
   END;
   ELSE
   DECLARE
      v_NomeEditora VARCHAR2(50);
   
   BEGIN
      SELECT Designação 

        INTO v_NomeEditora
        FROM Editoras 
