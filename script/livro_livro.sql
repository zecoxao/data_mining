v_incval NUMBER(12) := 0;
BEGIN
  IF INSERTING AND :new.Registo IS NULL THEN
    SELECT  Anomalias_Registo_SEQ.NEXTVAL INTO v_newVal FROM DUAL;
    -- If this is the first time this table have been inserted into (sequence == 1)
    IF v_newVal = 1 THEN 
      --get the max indentity value from the table
      SELECT NVL(max(Registo),0) INTO v_newVal FROM Anomalias;
      v_newVal := v_newVal + 1;
      --set the sequence to that value
      LOOP
           EXIT WHEN v_incval>=v_newVal;
           SELECT Anomalias_Registo_SEQ.nextval INTO v_incval FROM dual;
      END LOOP;
    END IF;
  -- save this to emulate @@identity
  utils.identity_value := v_newVal; 
   -- assign the value from the sequence to emulate the identity column
   :new.Registo := v_newVal;
  END IF;
END;
