   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE NOT EXISTS ( SELECT * 
                          FROM Livros 
                            WHERE Código = v_CódigoLivro );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
